//******************Postfix***************//
let x=2
const y= x++
console.log(`x:${x},y:${y}`);
//x++ means 
//Use the current value of x,then increment it
//y gets the current value whereas the value of x is incremented

//*****************Prefix******************//
let a=4
const b = ++a
console.log(`a:${a},b:${b}`);
//++x means
//increment a first then use the new value
// a is incremented and b gets the updated value
